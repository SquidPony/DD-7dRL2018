    private void doButtonAction(ActionEvent event) {
        Integer current = 0;
        try {
            current = Integer.parseInt(quantityText.getText());
        } catch (NumberFormatException nfe) {
            // Mute issue, change value to 0
        }
        switch (event.getActionCommand()) {
            case "plusOne":
                current += 1;
                break;
            case "plusTen":
                current += 10;
                break;
            case "multiplyTen":
                current *= 10;
                break;
        }
        quantityText.setText(current.toString());
    }

